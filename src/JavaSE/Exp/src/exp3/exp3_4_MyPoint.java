package JavaSE.Exp.exp3;

public class exp3_4_MyPoint {
     double x;//横坐标
     double y;//纵坐标

    exp3_4_MyPoint(){
        x=0;
        y=0;
    }

    exp3_4_MyPoint(double x, double y){
        this.x=x;
        this.y=y;
    }

    double getX() {
        return x;
    }

    double getY() {
        return y;
    }
}

